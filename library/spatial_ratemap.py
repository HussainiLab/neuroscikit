# This module will contain the spatial rate map class
# In addition to the bin & blur, and Hafting rate-maps, it will also contain
# a variety of structures designed to draw attention to the statistical
# properties of the ratemap, such as sample-sizes, statistical variation,
# and the like. It will also contain a variety of methods for assessing the
# true amount of spatial information in rate-maps. For example, it will have
# methods for simulating weakly stationary processes and assessing the
# information content of the simulated rate-maps compared to real rate-maps.